def parse_imagej_roi(filepath):
    """Parse ImageJ/Fiji point ROIs from a TIFF file.

    Uses tifffile to read ImageJ metadata and extracts point ROIs (type=10).
    Returns a list of (x, y) tuples, or an empty list if no ROIs found.
    """
    ext = os.path.splitext(filepath)[1].lower()
    if ext == ".czi":
        return []

    try:
        import importlib
        _tifffile = importlib.import_module('tifffile')
    except Exception:
        return []

    try:
        with _tifffile.TiffFile(filepath) as tif:
            if not hasattr(tif, 'imagej_metadata') or tif.imagej_metadata is None:
                return []
            roi_data = tif.imagej_metadata.get('ROI', None)
            if roi_data is None:
                return []

            # roi_data may be bytes or a single ROI dict depending on tifffile version
            if isinstance(roi_data, (bytes, bytearray)):
                rois_raw = [roi_data]
            elif isinstance(roi_data, list):
                rois_raw = roi_data
            else:
                return []

            points = []
            for raw in rois_raw:
                if not isinstance(raw, (bytes, bytearray)):
                    continue
                if len(raw) < 64:
                    continue

                # Header check: magic 'Iout'
                magic = raw[0:4]
                if magic != b'Iout':
                    continue

                # version = struct.unpack_from('>H', raw, 4)[0]
                roi_type = struct.unpack_from('B', raw, 6)[0]
                # padding at byte 7
                top = struct.unpack_from('>h', raw, 8)[0]
                left = struct.unpack_from('>h', raw, 10)[0]
                # bottom = struct.unpack_from('>h', raw, 12)[0]
                # right = struct.unpack_from('>h', raw, 14)[0]
                n_coords = struct.unpack_from('>H', raw, 16)[0]

                # Type 10 = point ROI
                if roi_type != 10:
                    continue
                if n_coords == 0:
                    continue

                # After the 64-byte header: x_offsets (n*2 bytes BE), then y_offsets (n*2 bytes BE)
                offset = 64
                if len(raw) < offset + n_coords * 4:
                    continue

                for i in range(n_coords):
                    x_off = struct.unpack_from('>h', raw, offset + i * 2)[0]
                    y_off = struct.unpack_from('>h', raw, offset + n_coords * 2 + i * 2)[0]
                    x = left + x_off
                    y = top + y_off
                    points.append((x, y))

            return points
    except Exception:
        return []


