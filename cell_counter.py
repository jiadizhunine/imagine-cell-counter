def read_image(filepath):
    """Read .czi or .tif/.tiff file, return (data_array, metadata_xml_or_None, annotations).

    annotations is a list of (x, y) tuples from Fiji ROI data, or empty list.
    """
    ext = os.path.splitext(filepath)[1].lower()
    annotations = parse_imagej_roi(filepath)

    if ext == ".czi":
        with czifile.CziFile(filepath) as czi:
            data = czi.asarray()
            meta_xml = czi.metadata()
        data = np.squeeze(data)
        if data.ndim == 2:
            data = data[np.newaxis, ...]
        if data.ndim != 3:
            raise ValueError(f"数据维度异常: {data.shape}")
        return data, meta_xml, annotations

    elif ext in (".tif", ".tiff"):
        import importlib
        _tifffile = importlib.import_module('tifffile')
        raw = _tifffile.imread(filepath)
        # Handle various shapes:
        # (H, W) -> single channel
        # (H, W, C) -> RGB/multi-channel interleaved
        # (C, H, W) -> multi-page TIFF (channels first)
        if raw.ndim == 2:
            data = raw[np.newaxis, ...]
        elif raw.ndim == 3:
            # If last dim is small (3 or 4), it's likely (H, W, C) interleaved
            if raw.shape[2] <= 4:
                data = np.moveaxis(raw, 2, 0)  # (H,W,C) -> (C,H,W)
            else:
                data = raw  # already (C, H, W)
        elif raw.ndim == 4:
            data = np.squeeze(raw)
            if data.ndim != 3:
                raise ValueError(f"数据维度异常: {raw.shape}")
        else:
            raise ValueError(f"数据维度异常: {raw.shape}")

        if data.dtype != np.uint8:
            # Normalize to uint8
            dmin, dmax = data.min(), data.max()
            if dmax > dmin:
                data = ((data.astype(np.float64) - dmin) / (dmax - dmin) * 255).astype(np.uint8)
            else:
                data = np.zeros_like(data, dtype=np.uint8)

        return data, None, annotations  # No XML metadata for TIFF
    else:
        raise ValueError(f"不支持的文件格式: {ext}")


def detect_channels(meta_xml, filepath=None):
    """Parse CZI XML metadata and return {role: channel_index}.
    If meta_xml is None (TIF files), use filename-based detection as primary.
    For CZI files, try XML metadata first, then fall back to filename detection.
    """
    mapping = {}

    if meta_xml is not None:
        try:
            root = ET.fromstring(meta_xml)
            for ch in root.iter("Channel"):
                ch_id = ch.get("Id", "")
                if not ch_id.startswith("Channel:"):
                    continue
                try:
                    idx = int(ch_id.split(":")[1])
                except (ValueError, IndexError):
                    continue
                color = ch.findtext("Color") or ""
                color = color.strip().upper()
                if color in COLOR_MAP_CZI:
                    role = COLOR_MAP_CZI[color]
                    if role not in mapping:
                        mapping[role] = idx
        except Exception:
            pass

    # If XML didn't provide enough info, try filename-based detection
    if len(mapping) < 4 and filepath:
        fn_info = detect_markers_from_filename(filepath)
        fn_map = fn_info["channel_map"]
        for role, idx in fn_map.items():
            if role not in mapping:
                mapping[role] = idx

    # Fallback: assume standard order if detection still incomplete
    if len(mapping) < 4:
        default = {0: "white", 1: "red", 2: "green", 3: "blue"}
        for idx in range(4):
            role = default.get(idx)
            if role and role not in mapping:
                mapping[role] = idx
    return mapping


def segment_nuclei(dapi, auto_thresh, manual_thresh, min_size, max_size=5000):
    """Segment nuclei from DAPI channel. Returns labelled array."""
    blurred = gaussian(dapi.astype(np.float64), sigma=3)

    if auto_thresh:
        try:
            otsu = threshold_otsu(blurred)
            # Use triangle method as fallback for skewed distributions
            from skimage.filters import threshold_triangle
            try:
                tri = threshold_triangle(blurred)
            except Exception:
                tri = otsu
            # Take the lower of Otsu and triangle — better for dim nuclei
            thresh = min(otsu, tri)
        except ValueError:
            thresh = manual_thresh
    else:
        thresh = manual_thresh / 255.0 * blurred.max() if blurred.max() > 0 else manual_thresh

    binary = blurred > thresh
    # Morphological opening to remove small noise before filling holes
    from scipy.ndimage import binary_opening
    binary = binary_opening(binary, iterations=1)
    binary = binary_fill_holes(binary)
    binary = remove_small_objects(binary, max_size=min_size)

    if not binary.any():
        return np.zeros_like(dapi, dtype=np.int32), 0

    dist = distance_transform_edt(binary)
    # local maxima as markers
    coords = peak_local_max(dist, min_distance=5, labels=binary)
    mask_markers = np.zeros(dist.shape, dtype=bool)
    if len(coords) > 0:
        mask_markers[coords[:, 0], coords[:, 1]] = True
    markers, _ = ndi_label(mask_markers)
    labels = watershed(-dist, markers, mask=binary)

    # Filter by size (vectorized)
    areas = np.bincount(labels.ravel())
    too_small_or_large = np.where((areas < 30) | (areas > max_size))[0]
    too_small_or_large = too_small_or_large[too_small_or_large > 0]
    labels[np.isin(labels, too_small_or_large)] = 0

    # Relabel (vectorized)
    unique = np.unique(labels)
    unique = unique[unique > 0]
    remap = np.zeros(labels.max() + 1, dtype=np.int32)
    remap[unique] = np.arange(1, len(unique) + 1)
    new_labels = remap[labels]

    return new_labels, int(new_labels.max())


def count_yap(labels, red_channel, threshold):
    """Count YAP+ nuclei: red signal co-localised on DAPI nuclei."""
    n = labels.max()
    if n == 0:
        return []
    means = ndimage.mean(red_channel, labels, range(1, n + 1))
    return [i + 1 for i, m in enumerate(means) if m >= threshold]


def count_at2(labels, green_channel, threshold, ring_width):
    """Count AT2 cells: green cytoplasm ring around nuclei (bbox-optimized)."""
    n = labels.max()
    if n == 0:
        return []
    slices = ndimage.find_objects(labels)
    pad = ring_width + 2
    h, w = labels.shape
    positives = []
    struct = ndimage.generate_binary_structure(2, 1)
    for lbl_idx, sl in enumerate(slices):
        if sl is None:
            continue
        lbl = lbl_idx + 1
        r0 = max(0, sl[0].start - pad)
        r1 = min(h, sl[0].stop + pad)
        c0 = max(0, sl[1].start - pad)
        c1 = min(w, sl[1].stop + pad)
        patch_labels = labels[r0:r1, c0:c1]
        patch_mask = patch_labels == lbl
        dilated = ndimage.binary_dilation(patch_mask, structure=struct, iterations=ring_width)
        ring = dilated & ~patch_mask
        if ring.any():
            mean_val = green_channel[r0:r1, c0:c1][ring].mean()
            if mean_val >= threshold:
                positives.append(lbl)
    return positives


def make_composite(data, channel_map, show_channels, contrast_pct=(0.5, 99.8)):
    """Build RGBA composite from selected channels with percentile contrast."""
    h, w = data.shape[1], data.shape[2]
    composite = np.zeros((h, w, 3), dtype=np.float64)

    def enhance(img):
        lo = np.percentile(img, contrast_pct[0])
        hi = np.percentile(img, contrast_pct[1])
        if hi <= lo:
            hi = lo + 1
        return np.clip((img.astype(np.float64) - lo) / (hi - lo), 0, 1)

    if show_channels.get("blue") and "blue" in channel_map:
        ch = enhance(data[channel_map["blue"]])
        composite[:, :, 2] += ch

    if show_channels.get("red") and "red" in channel_map:
        ch = enhance(data[channel_map["red"]])
        composite[:, :, 0] += ch

    if show_channels.get("green") and "green" in channel_map:
        ch = enhance(data[channel_map["green"]])
        composite[:, :, 1] += ch

    if show_channels.get("white") and "white" in channel_map:
        ch = enhance(data[channel_map["white"]])
        composite[:, :, 0] += ch * 0.7
        composite[:, :, 1] += ch * 0.7
        composite[:, :, 2] += ch * 0.7

    composite = np.clip(composite * 255, 0, 255).astype(np.uint8)
    return composite


def draw_overlays(composite, labels, yap_labels, at2_labels, show_nuclei, show_yap, show_at2):
    """Draw contour overlays on composite image."""
    from scipy.ndimage import binary_erosion

    overlay = composite.copy()
    if labels.max() == 0:
        return overlay

    all_mask = labels > 0
    eroded_all = binary_erosion(all_mask, iterations=1)
    contour_all = all_mask & ~eroded_all

    if show_nuclei:
        overlay[contour_all] = [255, 255, 255]

    if show_yap and yap_labels:
        yap_set = set(yap_labels)
        yap_mask = np.isin(labels, list(yap_set))
        yap_contour = yap_mask & ~binary_erosion(yap_mask, iterations=1)
        overlay[yap_contour] = [255, 80, 80]

    if show_at2 and at2_labels:
        at2_set = set(at2_labels)
        at2_mask = np.isin(labels, list(at2_set))
        at2_contour = at2_mask & ~binary_erosion(at2_mask, iterations=1)
        overlay[at2_contour] = [80, 255, 80]

    return overlay


def analyze_czi(filepath, params, progress_fn=None):
    """Full analysis pipeline. Returns result dict."""
    t0 = time.time()
    if progress_fn:
        progress_fn(f"Reading: {os.path.basename(filepath)}")
    data, meta_xml, annotations = read_image(filepath)

    # Filename-based marker detection
    fn_info = detect_markers_from_filename(filepath)
    detected_markers = fn_info["markers"]
    applicable_strategies = determine_strategy(detected_markers)

    # Channel mapping: use user overrides if provided, else auto-detect
    auto_map = detect_channels(meta_xml, filepath)
    channel_map = {}
    for role in CHANNEL_ROLES:
        override = params.get(f"ch_{role}")
        if override is not None and override >= 0:
            channel_map[role] = override
        elif role in auto_map:
            channel_map[role] = auto_map[role]

    if "blue" not in channel_map:
        raise ValueError("DAPI (blue) channel not detected")

    dapi = data[channel_map["blue"]]

    if progress_fn:
        progress_fn("Segmenting nuclei...")
    labels, total = segment_nuclei(
        dapi,
        params.get("auto_thresh", True),
        params.get("dapi_thresh", 128),
        params.get("min_nucleus", 50),
    )

    # Legacy YAP+ counting (kept for backward compat)
    yap_labels = []
    if "red" in channel_map:
        if progress_fn:
            progress_fn("Counting YAP+ cells...")
        yap_labels = count_yap(labels, data[channel_map["red"]], params.get("red_thresh", 15))

    # Legacy AT2 counting (kept for backward compat)
    at2_labels = []
    if "green" in channel_map:
        if progress_fn:
            progress_fn("Counting AT2 cells...")
        at2_labels = count_at2(
            labels,
            data[channel_map["green"]],
            params.get("green_thresh", 10),
            params.get("ring_width", 15),
        )

    # Run applicable strategies
    strategy_results = []
    for skey in applicable_strategies:
        strat = STRATEGIES[skey]
        if progress_fn:
            progress_fn(f"策略: {strat['name']}...")

        marker_ch = strat.get("marker_channel")
        if marker_ch not in channel_map:
            continue

        if strat["method"] == "count_colocalized":
            if marker_ch == "red":
                thresh_key = "red_thresh"
            elif marker_ch == "white":
                thresh_key = "white_thresh"
            else:
                thresh_key = "green_thresh"
            positive_labels = count_colocalized(
                labels, data[channel_map[marker_ch]], params.get(thresh_key, 15)
            )
            pct = (len(positive_labels) / total * 100) if total > 0 else 0
            strategy_results.append({
                "key": skey,
                "name": strat["name"],
                "desc": strat["desc"],
                "positive_count": len(positive_labels),
                "total": total,
                "percentage": pct,
                "positive_labels": positive_labels,
            })

        elif strat["method"] == "count_colocalized_filtered":
            # Nuclear detection with median filtering (for HOPX etc.)
            if marker_ch == "red":
                thresh_key = "red_thresh"
            elif marker_ch == "white":
                thresh_key = "white_thresh"
            else:
                thresh_key = "green_thresh"
            positive_labels = count_colocalized_filtered(
                labels, data[channel_map[marker_ch]], params.get(thresh_key, 15),
            )
            pct = (len(positive_labels) / total * 100) if total > 0 else 0
            strategy_results.append({
                "key": skey,
                "name": strat["name"],
                "desc": strat["desc"],
                "positive_count": len(positive_labels),
                "total": total,
                "percentage": pct,
                "positive_labels": positive_labels,
            })

        elif strat["method"] == "count_double_positive":
            second_ch = strat.get("second_channel")
            if second_ch not in channel_map:
                continue
            if marker_ch == "red":
                marker_thresh_key = "red_thresh"
            elif marker_ch == "white":
                marker_thresh_key = "white_thresh"
            else:
                marker_thresh_key = "green_thresh"
            if second_ch == "red":
                second_thresh_key = "red_thresh"
            elif second_ch == "white":
                second_thresh_key = "white_thresh"
            else:
                second_thresh_key = "green_thresh"
            positive_labels = count_double_positive(
                labels,
                data[channel_map[marker_ch]],
                data[channel_map[second_ch]],
                params.get(marker_thresh_key, 15),
                params.get(second_thresh_key, 15),
            )
            # For double positive, percentage is relative to total nuclei
            pct = (len(positive_labels) / total * 100) if total > 0 else 0
            strategy_results.append({
                "key": skey,
                "name": strat["name"],
                "desc": strat["desc"],
                "positive_count": len(positive_labels),
                "total": total,
                "percentage": pct,
                "positive_labels": positive_labels,
            })

    elapsed = time.time() - t0

    return {
        "filepath": filepath,
        "filename": os.path.basename(filepath),
        "data": data,
        "channel_map": channel_map,
        "labels": labels,
        "total_nuclei": total,
        "yap_labels": yap_labels,
        "yap_count": len(yap_labels),
        "yap_pct": (len(yap_labels) / total * 100) if total > 0 else 0,
        "at2_labels": at2_labels,
        "at2_count": len(at2_labels),
        "at2_pct": (len(at2_labels) / total * 100) if total > 0 else 0,
        "elapsed": elapsed,
        "annotations": annotations,
        "detected_markers": detected_markers,
        "applicable_strategies": applicable_strategies,
        "strategy_results": strategy_results,
    }


