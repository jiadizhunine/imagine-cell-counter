    # ----- BUG 1: Update channel assignment labels -----
    def _update_channel_labels(self, markers):
        """Update channel combo labels based on detected markers.
        markers: dict like {"Ki67": "red", "ProSPC": "white", "HOPX": "green"}
        """
        # Build reverse map: role -> marker_name
        role_to_marker = {}
        for marker_name, role in markers.items():
            role_to_marker[role] = marker_name

        ROLE_CN = {"blue": "蓝色", "red": "红色", "green": "绿色", "white": "白色"}
        for role, lbl_widget in self._ch_label_widgets.items():
            cn = ROLE_CN.get(role, role)
            marker = role_to_marker.get(role)
            if role == "blue":
                lbl_widget.setText(f"{cn} (DAPI)")
            elif marker:
                lbl_widget.setText(f"{cn} ({marker})")
            else:
                lbl_widget.setText(cn)

    # ----- FEATURE 3: Dynamic sidebar -----
    def _update_sidebar_for_strategy(self, markers, strategies):
        """Show/hide and relabel sidebar threshold groups based on detected markers/strategies."""
        role_to_marker = {}
        for marker_name, role in markers.items():
            role_to_marker[role] = marker_name

        # Red group
        red_marker = role_to_marker.get("red")
        if red_marker:
            self._red_group.setTitle(f"{red_marker} 阈值 (红色)")
        else:
            self._red_group.setTitle("红色阈值 (红色)")
        self._red_group.show()

        # Green group
        green_marker = role_to_marker.get("green")
        if green_marker:
            self._green_group.setTitle(f"{green_marker} 阈值 (绿色)")
        else:
            self._green_group.setTitle("绿色阈值 (绿色)")
        self._green_group.show()

        # White group - only show when a white channel marker is detected
        white_marker = role_to_marker.get("white")
        if white_marker:
            self._white_group.setTitle(f"{white_marker} 阈值 (白色)")
            self._white_group.show()
        else:
            self._white_group.hide()

        # Ring width - only show when AT2 or AT1 analysis needs cytoplasm ring detection
        # Legacy AT2 counting uses ring, and any strategy with ring-based methods would too
        needs_ring = False
        for skey in strategies:
            strat = STRATEGIES.get(skey, {})
            # If the strategy uses count_at2 or has "AT2" or "AT1" in the key, ring may be needed
            if "AT2" in skey or "AT1" in skey:
                needs_ring = True
                break
        # Also show ring if we have green channel (legacy AT2 mode)
        if not strategies and "green" in role_to_marker:
            needs_ring = True
        if needs_ring:
            self._ring_group.show()
        else:
            self._ring_group.hide()

    # ----- FEATURE 4: Dynamic overlay bar -----
    def _update_overlay_bar(self, strategies):
        """Rebuild overlay toggle buttons based on detected strategies."""
        # Clear old strategy overlay buttons
        for btn in self._strategy_overlay_btns.values():
            self._strategy_overlay_layout.removeWidget(btn)
            btn.deleteLater()
        self._strategy_overlay_btns.clear()

