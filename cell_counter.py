    def _refresh_display(self):
        if self.current_result is None:
            return
        r = self.current_result
        if "data" not in r or "labels" not in r:
            return
        show = {role: self.ch_toggles[role].isChecked() for role in CHANNEL_ROLES}
        composite = make_composite(r["data"], r["channel_map"], show)

        # Build strategy overlay visibility map
        strategy_show = {}
        for skey, btn in self._strategy_overlay_btns.items():
            strategy_show[skey] = btn.isChecked()

        # Legacy overlays only shown when legacy buttons are active in strategy_show
        show_yap = strategy_show.get("__legacy_yap", False)
        show_at2 = strategy_show.get("__legacy_at2", False)

        overlay = draw_overlays(
            composite,
            r["labels"],
            r["yap_labels"],
            r["at2_labels"],
            self.show_nuclei_btn.isChecked(),
            show_yap,
            show_at2,
        )

        # Draw strategy-specific overlays
        from scipy.ndimage import binary_erosion
        strategy_results = r.get("strategy_results", [])
        for sr in strategy_results:
            skey = sr["key"]
            if not strategy_show.get(skey, False):
                continue
            positive_labels = sr.get("positive_labels", [])
            if not positive_labels:
                continue
            color = STRATEGY_COLORS.get(skey, "#e0e0e0")
            # Parse hex color to RGB
            c = color.lstrip("#")
            rgb = [int(c[i:i+2], 16) for i in (0, 2, 4)]
            mask = np.isin(r["labels"], positive_labels)
            contour = mask & ~binary_erosion(mask, iterations=1)
            overlay[contour] = rgb

        h, w, _ = overlay.shape
        qimg = QImage(overlay.data.tobytes(), w, h, 3 * w, QImage.Format_RGB888)
        self.viewer.update_pixmap(QPixmap.fromImage(qimg))

    def _add_to_table(self, result):
        row = self.results_table.rowCount()
        self.results_table.insertRow(row)
        items = [
            result["filename"],
            str(result["total_nuclei"]),
            str(result["yap_count"]),
            f"{result['yap_pct']:.1f}",
            str(result["at2_count"]),
            f"{result['at2_pct']:.1f}",
        ]
        for col, text in enumerate(items):
            item = QTableWidgetItem(text)
            if col > 0:
                item.setTextAlignment(Qt.AlignCenter)
            # FEATURE 5: Store filepath as UserRole data on column 0
            if col == 0:
                item.setData(Qt.UserRole, result.get("filepath", ""))
            self.results_table.setItem(row, col, item)

        # Store minimal result for export (strip large arrays, strip positive_labels from strategies)
        export_result = {k: v for k, v in result.items() if k not in ("data", "labels")}
        # Strip positive_labels from strategy_results to save memory
        if "strategy_results" in export_result:
            export_result["strategy_results"] = [
                {k: v for k, v in sr.items() if k != "positive_labels"}
                for sr in export_result["strategy_results"]
            ]
        self.batch_results.append(export_result)

        # Cache result by filepath for double-click restore
        filepath = result.get("filepath", "")
        if filepath:
            self._batch_results_by_path[filepath] = result

    # ----- Batch -----
    def batch_process(self):
        folder = QFileDialog.getExistingDirectory(self, "选择包含图像文件的文件夹")
        if not folder:
            return
        files = sorted(
            list(Path(folder).glob("*.czi")) +
            list(Path(folder).glob("*.tif")) +
            list(Path(folder).glob("*.tiff"))
        )
        if not files:
            QMessageBox.information(self, "提示", "该文件夹中未找到 .czi/.tif 文件")
            return

        self.analyze_btn.setEnabled(False)
        self.progress_bar.setRange(0, len(files))
        self.progress_bar.setValue(0)
        self.progress_bar.show()
        self.status_label.setText(f"批量处理: 0/{len(files)}")

        # FEATURE 6: Populate file list for navigation
        file_strs = [str(f) for f in files]
        for fp in file_strs:
            if fp not in self._file_list:
                self._file_list.append(fp)
        self._update_nav_label()

        params = self._get_params()
        self.batch_worker = BatchWorker(file_strs, params)
        self.batch_worker.file_done.connect(self._on_batch_file_done)
        self.batch_worker.progress_count.connect(self._on_batch_progress)
        self.batch_worker.error.connect(self._on_batch_error)
        self.batch_worker.all_done.connect(self._on_batch_done)
        self.batch_worker.start()

    def _on_batch_file_done(self, result):
        self._add_to_table(result)
        # Show last processed image as current
        self.current_result = result
        self._current_file = result.get("filepath", self._current_file)
        self.filename_label.setText(result.get("filename", ""))

        # Update dynamic UI elements for current batch file
        detected_markers = result.get("detected_markers", {})
        applicable = result.get("applicable_strategies", [])
        self._update_channel_labels(detected_markers)
        self._update_sidebar_for_strategy(detected_markers, applicable)
        self._update_overlay_bar(applicable)

        self._update_results_display(result)
        self._refresh_display()

        # Update file navigation
        if self._current_file and self._current_file not in self._file_list:
            self._file_list.append(self._current_file)
        if self._current_file in self._file_list:
            self._file_index = self._file_list.index(self._current_file)
        self._update_nav_label()

    def _on_batch_progress(self, current, total):
        self.progress_bar.setValue(current)
        self.status_label.setText(f"批量处理: {current}/{total}")

    def _on_batch_error(self, filepath, msg):
        self.statusBar().showMessage(f"错误: {os.path.basename(filepath)} - {msg}")

    def _on_batch_done(self):
        self.analyze_btn.setEnabled(True)
        self.analyze_all_btn.setEnabled(True)
        self.progress_bar.hide()
        n = self.progress_bar.maximum()
        self.status_label.setText("批量处理完成")
        self.statusBar().showMessage(f"批量处理完成: 共处理 {n} 个文件")
        QMessageBox.information(self, "完成", f"已处理 {n} 个文件")

    # ----- Export -----
    def export_csv(self):
        if not self.batch_results:
            QMessageBox.information(self, "提示", "没有可导出的结果")
            return
        filepath, _ = QFileDialog.getSaveFileName(
            self, "导出 CSV", "cell_counts.csv", "CSV 文件 (*.csv)"
        )
        if not filepath:
            return

        # Collect all strategy keys that appeared in any result
        all_strategy_keys = []
        seen = set()
        for r in self.batch_results:
            for sr in r.get("strategy_results", []):
                if sr["key"] not in seen:
                    seen.add(sr["key"])
                    all_strategy_keys.append(sr["key"])

        # Determine if legacy columns are needed
        has_legacy = any(not r.get("applicable_strategies") for r in self.batch_results)

        # Build dynamic header
        header = ["Filename", "Total_Nuclei"]
        if has_legacy:
            header += ["YAP_Positive", "YAP_Percent", "AT2_Cells", "AT2_Percent"]
        strategy_col_map = {}  # key -> (count_col_name, pct_col_name)
        for skey in all_strategy_keys:
            strat = STRATEGIES.get(skey, {})
            base_name = skey  # e.g. AT2_number
            count_col = f"{base_name}_Count"
            pct_col = f"{base_name}_Percent"
            header.append(count_col)
            header.append(pct_col)
            strategy_col_map[skey] = (count_col, pct_col)
        header.append("Annotations")

        with open(filepath, "w", newline="", encoding="utf-8-sig") as f:
            writer = csv.writer(f)
            writer.writerow(header)
            for r in self.batch_results:
                ann_count = len(r.get("annotations", []))
                row = [
                    r["filename"],
                    r["total_nuclei"],
                ]
                if has_legacy:
                    row += [
                        r["yap_count"],
                        f"{r['yap_pct']:.1f}",
                        r["at2_count"],
                        f"{r['at2_pct']:.1f}",
                    ]
                # Build a lookup from strategy key to result
                sr_lookup = {sr["key"]: sr for sr in r.get("strategy_results", [])}
                for skey in all_strategy_keys:
                    if skey in sr_lookup:
                        row.append(sr_lookup[skey]["positive_count"])
                        row.append(f"{sr_lookup[skey]['percentage']:.1f}")
                    else:
                        row.append("")
                        row.append("")
                row.append(ann_count)
                writer.writerow(row)

            # Write threshold parameters
            writer.writerow([])  # blank row
            writer.writerow(["=== 分析参数 ==="])
            params = self._get_params()
            writer.writerow(["自动阈值(Otsu)", "是" if params["auto_thresh"] else "否"])
            writer.writerow(["DAPI阈值", params["dapi_thresh"]])
            writer.writerow(["红色通道阈值", params["red_thresh"]])
            writer.writerow(["绿色通道阈值", params["green_thresh"]])
            writer.writerow(["白色通道阈值", params["white_thresh"]])
            writer.writerow(["环宽度", params["ring_width"]])
            writer.writerow(["最小核面积", params["min_nucleus"]])

        self.statusBar().showMessage(f"已导出: {filepath}")
        QMessageBox.information(self, "导出成功", f"结果已保存至:\n{filepath}")


