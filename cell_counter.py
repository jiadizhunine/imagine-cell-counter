class AnalysisWorker(QThread):
    finished = pyqtSignal(dict)
    progress = pyqtSignal(str)
    error = pyqtSignal(str)

    def __init__(self, filepath, params):
        super().__init__()
        self.filepath = filepath
        self.params = params

    def run(self):
        try:
            result = analyze_czi(self.filepath, self.params, self.progress.emit)
            self.finished.emit(result)
        except Exception as e:
            self.error.emit(str(e))


class BatchWorker(QThread):
    file_done = pyqtSignal(dict)
    all_done = pyqtSignal()
    progress_count = pyqtSignal(int, int)
    error = pyqtSignal(str, str)

    def __init__(self, file_list, params):
        super().__init__()
        self.file_list = file_list
        self.params = params

    def run(self):
        for i, fp in enumerate(self.file_list):
            self.progress_count.emit(i + 1, len(self.file_list))
            try:
                result = analyze_czi(fp, self.params)
                self.file_done.emit(result)
            except Exception as e:
                self.error.emit(fp, str(e))
        self.all_done.emit()


