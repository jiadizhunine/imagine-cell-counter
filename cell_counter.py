def detect_markers_from_filename(filepath):
    """Parse the filename to identify markers and channel mapping.

    First tries specific known patterns (Ki67, ProSPC, HOPX, aSMA, GFP),
    then falls back to generic MARKER-WAVELENGTH patterns (e.g. CD45-568, CD31-488).
    """
    basename = os.path.basename(filepath)
    name_no_ext = os.path.splitext(basename)[0]

    markers = {}
    channel_map = {}

    # Check if DAPI is mentioned in the filename
    if re.search(r'\bDAPI\b', name_no_ext, re.IGNORECASE):
        channel_map["blue"] = 3

    # Try specific known patterns first
    for pattern, marker_name, wavelength in MARKER_PATTERNS:
        if pattern.search(name_no_ext):
            channel_color = WAVELENGTH_CHANNEL.get(wavelength)
            if channel_color:
                markers[marker_name] = channel_color

    # Generic fallback: detect any MARKER-WAVELENGTH patterns not already found
    for match in GENERIC_MARKER_RE.finditer(name_no_ext):
        marker_name = match.group(1)
        wavelength = match.group(2)
        channel_color = WAVELENGTH_CHANNEL.get(wavelength)
        if channel_color and channel_color not in markers.values():
            # Don't overwrite already-detected markers for this color
            markers[marker_name] = channel_color

    # Also detect standalone wavelengths for channel_map
    if re.search(r'568', name_no_ext):
        channel_map["red"] = 1
    if re.search(r'647', name_no_ext):
        channel_map["white"] = 0
    if re.search(r'488', name_no_ext):
        channel_map["green"] = 2

    # Default: if DAPI not found but other channels detected, assume DAPI exists
    if "blue" not in channel_map:
        channel_map["blue"] = 3

    return {
        "markers": markers,
        "channel_map": channel_map,
    }


def determine_strategy(markers):
    """Given a markers dict, return a list of applicable strategy keys."""
    applicable = []
    for key, strategy in STRATEGIES.items():
        if all(req in markers for req in strategy["requires"]):
            applicable.append(key)
    return applicable


def count_colocalized(labels, marker_data, threshold):
    """For each nucleus, check if marker signal mean intensity >= threshold.
    Returns list of positive label IDs."""
    n = labels.max()
    if n == 0:
        return []
    means = ndimage.mean(marker_data, labels, range(1, n + 1))
    return [i + 1 for i, m in enumerate(means) if m >= threshold]


def count_colocalized_filtered(labels, marker_data, threshold):
    """Nuclear marker detection with no-signal-image rejection.

    For nuclear markers like HOPX, the main problem is images WITHOUT real
    signal still showing hundreds of false positives due to tissue
    autofluorescence. Solution:

    1. Raw threshold gate (user's slider) — preserves ALL true positives
    2. Morphological reconstruction background floor — in images with NO
       real signal, corrected values are ~0 and all candidates are rejected.
       In images WITH real signal, this filter is transparent (all cells
       that pass raw threshold also have corrected > floor).
    3. Local ring contrast (1.5x) on raw signal — additional FP reduction.

    This ensures: signal images → all TP kept; no-signal images → 0 FP.
    """
    from scipy.ndimage import median_filter
    from skimage.morphology import disk, reconstruction, erosion as morph_erosion

    n = labels.max()
    if n == 0:
        return []

    # Raw signal processing
    filtered = median_filter(marker_data, size=3)
    raw_means = ndimage.mean(filtered, labels, range(1, n + 1))

    # Gate 1: Raw threshold (user's slider — keeps all true positives)
    candidates = [i for i in range(n) if raw_means[i] >= threshold]
    if not candidates:
        return []

    # Gate 2: Morphological reconstruction background floor
    # This eliminates false positives in images with NO real signal
    marker_f64 = marker_data.astype(np.float64)
    seed = morph_erosion(marker_f64, disk(12))
    background = reconstruction(seed, marker_f64, method='dilation')
    corrected = np.clip(marker_f64 - background, 0, None)
    corr_filtered = median_filter(corrected, size=3)
    corr_means = ndimage.mean(corr_filtered, labels, range(1, n + 1))
    corr_floor = 2.0  # very low — only rejects pure background-on-background
    candidates = [i for i in candidates if corr_means[i] >= corr_floor]
    if not candidates:
        return []

    # Gate 3: Local ring contrast on raw signal (1.5x)
    slices = ndimage.find_objects(labels)
    h, w = labels.shape
    struct = ndimage.generate_binary_structure(2, 1)
    ring_width = 10
    pad = ring_width + 2
    contrast_ratio = 1.5

    positives = []
    for lbl_idx in candidates:
        sl = slices[lbl_idx]
        if sl is None:
            continue
        lbl = lbl_idx + 1
        r0 = max(0, sl[0].start - pad)
        r1 = min(h, sl[0].stop + pad)
        c0 = max(0, sl[1].start - pad)
        c1 = min(w, sl[1].stop + pad)
        patch_labels = labels[r0:r1, c0:c1]
        dilated = ndimage.binary_dilation(
            patch_labels == lbl, structure=struct, iterations=ring_width)
        ring = dilated & (patch_labels == 0)
        if ring.any():
            bg_mean = filtered[r0:r1, c0:c1][ring].mean()
        else:
            bg_mean = 0
        if bg_mean < 1 or raw_means[lbl_idx] / bg_mean >= contrast_ratio:
            positives.append(lbl)

    return positives


def count_double_positive(labels, marker_data, second_data, marker_thresh, second_thresh):
    """For each nucleus, check if BOTH marker AND second channel signals are
    above thresholds. Returns list of double-positive label IDs.
    Applies median filtering to reduce noise from staining artifacts."""
    from scipy.ndimage import median_filter
    n = labels.max()
    if n == 0:
        return []
    # Median filter both channels to reduce floating color / staining artifacts
    marker_filtered = median_filter(marker_data, size=3)
    second_filtered = median_filter(second_data, size=3)
    marker_means = ndimage.mean(marker_filtered, labels, range(1, n + 1))
    second_means = ndimage.mean(second_filtered, labels, range(1, n + 1))
    return [i + 1 for i, (mm, sm) in enumerate(zip(marker_means, second_means))
            if mm >= marker_thresh and sm >= second_thresh]


