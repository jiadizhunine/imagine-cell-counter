class ImageViewer(QWidget):
    zoom_changed = pyqtSignal(float)
    annotations_changed = pyqtSignal()

    def __init__(self, parent=None):
        super().__init__(parent)
        self._pixmap = QPixmap()
        self._zoom = 1.0
        self._offset = QPointF(0, 0)
        self._drag_start = None
        self._drag_offset = QPointF(0, 0)
        self._annotations = []      # list of (x, y, color_tag) in image coordinates
        self._current_tag = 0       # index into TAG_COLORS
        self._annotation_mode = False
        self._undo_stack = []
        self._redo_stack = []
        self.setMinimumSize(400, 400)
        self.setSizePolicy(QSizePolicy.Expanding, QSizePolicy.Expanding)
        self.setMouseTracking(True)

    # --- Annotation properties and methods ---
    @property
    def annotation_mode(self):
        return self._annotation_mode

    @annotation_mode.setter
    def annotation_mode(self, value):
        self._annotation_mode = bool(value)

    def set_annotations(self, points):
        """Set annotations from loaded Fiji data.
        Accepts both (x,y) and (x,y,tag) tuples for backward compatibility.
        Old-style (x,y) tuples are converted to (x,y,0).
        """
        self._undo_stack.append(list(self._annotations))
        self._redo_stack.clear()
        normalized = []
        for p in points:
            if len(p) == 3:
                normalized.append(tuple(p))
            else:
                normalized.append((p[0], p[1], 0))
        self._annotations = normalized
        self.update()
        self.annotations_changed.emit()

    def get_annotations(self):
        """Return current annotation points as list of (x, y, tag) tuples."""
        return list(self._annotations)

    def clear_annotations(self):
        """Clear all annotation points."""
        self._undo_stack.append(list(self._annotations))
        self._redo_stack.clear()
        self._annotations.clear()
        self.update()
        self.annotations_changed.emit()

    def undo(self):
        """Undo last annotation change."""
        if self._undo_stack:
            self._redo_stack.append(list(self._annotations))
            self._annotations = self._undo_stack.pop()
            self.update()
            self.annotations_changed.emit()

    def redo(self):
        """Redo last undone annotation change."""
        if self._redo_stack:
            self._undo_stack.append(list(self._annotations))
            self._annotations = self._redo_stack.pop()
            self.update()
            self.annotations_changed.emit()

    def set_pixmap(self, pixmap: QPixmap):
        self._pixmap = pixmap
        self._zoom = 1.0
        self._offset = QPointF(0, 0)
        self.update()
        self.zoom_changed.emit(self._zoom)

    def update_pixmap(self, pixmap: QPixmap):
        """Update displayed pixmap without resetting zoom/offset."""
        self._pixmap = pixmap
        self.update()

    def _screen_to_image(self, screen_pos):
        """Convert a screen (widget) position to image pixel coordinates."""
        img_x = (screen_pos.x() - self._offset.x()) / self._zoom
        img_y = (screen_pos.y() - self._offset.y()) / self._zoom
        return img_x, img_y

    def _image_to_screen(self, img_x, img_y):
        """Convert image pixel coordinates to screen (widget) position."""
        sx = img_x * self._zoom + self._offset.x()
        sy = img_y * self._zoom + self._offset.y()
        return sx, sy

    def paintEvent(self, event):
        if self._pixmap.isNull():
            return
        painter = QPainter(self)
        painter.setRenderHint(QPainter.SmoothPixmapTransform)
        painter.translate(self._offset)
        painter.scale(self._zoom, self._zoom)
        painter.drawPixmap(0, 0, self._pixmap)

        # Reset transform for annotations (draw in screen coordinates)
        painter.resetTransform()

        if self._annotations:
            cross_size = 8
            painter.setRenderHint(QPainter.Antialiasing)

            font = QFont()
            font.setPixelSize(11)
            font.setBold(True)
            painter.setFont(font)

            # Group annotations by tag for independent numbering
            tag_counters = {}  # tag_index -> running count
            for ann in self._annotations:
                # Handle both old-style (x,y) and new (x,y,tag) tuples
                if len(ann) == 3:
                    ix, iy, tag = ann
                else:
                    ix, iy = ann[0], ann[1]
                    tag = 0

                tag_counters[tag] = tag_counters.get(tag, 0) + 1
                idx_in_tag = tag_counters[tag]

                # Get color for this tag
                if 0 <= tag < len(TAG_COLORS):
                    ann_color = QColor(TAG_COLORS[tag][0])
                else:
                    ann_color = QColor("#FFD700")
                pen = QPen(ann_color, 2)

                sx, sy = self._image_to_screen(ix, iy)

                # Draw cross (+)
                painter.setPen(pen)
                painter.drawLine(
                    int(sx - cross_size), int(sy),
                    int(sx + cross_size), int(sy),
                )
                painter.drawLine(
                    int(sx), int(sy - cross_size),
                    int(sx), int(sy + cross_size),
                )

                # Draw number label with dark background
                label_text = str(idx_in_tag)
                fm = painter.fontMetrics()
                text_w = fm.horizontalAdvance(label_text) + 4
                text_h = fm.height() + 2
                label_x = int(sx + cross_size + 3)
                label_y = int(sy - text_h / 2)

                # Background rect
                painter.setPen(Qt.NoPen)
                painter.setBrush(QColor(0, 0, 0, 180))
                painter.drawRoundedRect(label_x - 1, label_y, text_w + 2, text_h, 2, 2)

                # Text
                painter.setPen(QColor(255, 255, 255))
                painter.drawText(label_x + 1, label_y + fm.ascent() + 1, label_text)

        painter.end()

    def wheelEvent(self, event: QWheelEvent):
        old_zoom = self._zoom
        factor = 1.15 if event.angleDelta().y() > 0 else 1.0 / 1.15
        self._zoom *= factor
        self._zoom = max(0.05, min(self._zoom, 50.0))
        mouse_pos = event.pos()
        self._offset = QPointF(
            mouse_pos.x() - (mouse_pos.x() - self._offset.x()) * self._zoom / old_zoom,
            mouse_pos.y() - (mouse_pos.y() - self._offset.y()) * self._zoom / old_zoom,
        )
        self.update()
        self.zoom_changed.emit(self._zoom)

    def mousePressEvent(self, event):
        if event.button() == Qt.LeftButton:
            self._drag_start = event.pos()
            self._drag_offset = QPointF(self._offset)
        elif event.button() == Qt.RightButton and self._annotation_mode:
            self._handle_annotation_click(event.pos())

    def _handle_annotation_click(self, pos):
        """Handle right-click for adding/removing annotation points."""
        img_x, img_y = self._screen_to_image(pos)

        # Check if clicking near an existing annotation (within 10px screen radius)
        remove_idx = -1
        for idx, ann in enumerate(self._annotations):
            ax, ay = ann[0], ann[1]
            sx, sy = self._image_to_screen(ax, ay)
            dist = ((pos.x() - sx) ** 2 + (pos.y() - sy) ** 2) ** 0.5
            if dist <= 10:
                remove_idx = idx
                break

        # Push current state to undo stack before modifying
        self._undo_stack.append(list(self._annotations))
        self._redo_stack.clear()

        if remove_idx >= 0:
            self._annotations.pop(remove_idx)
        else:
            # Only add if within image bounds
            if not self._pixmap.isNull():
                pw = self._pixmap.width()
                ph = self._pixmap.height()
                if 0 <= img_x < pw and 0 <= img_y < ph:
                    self._annotations.append((int(round(img_x)), int(round(img_y)), self._current_tag))

        self.update()
        self.annotations_changed.emit()

    def mouseMoveEvent(self, event):
        if self._drag_start is not None:
            delta = event.pos() - self._drag_start
            self._offset = self._drag_offset + QPointF(delta.x(), delta.y())
            self.update()

    def mouseReleaseEvent(self, event):
        self._drag_start = None


