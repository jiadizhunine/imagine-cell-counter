    def _build_imagej_roi_bytes(self, points):
        """Build ImageJ ROI binary data for point annotations."""
        if not points:
            return None
        n = len(points)
        xs = [int(p[0]) for p in points]
        ys = [int(p[1]) for p in points]
        top = min(ys)
        left = min(xs)
        bottom = max(ys)
        right = max(xs)

        # Header: 64 bytes
        header = bytearray(64)
        header[0:4] = b'Iout'                                    # magic
        struct.pack_into('>h', header, 4, 228)                   # version
        header[6] = 10                                            # type = point
        header[7] = 0                                             # padding
        struct.pack_into('>h', header, 8, top)
        struct.pack_into('>h', header, 10, left)
        struct.pack_into('>h', header, 12, bottom)
        struct.pack_into('>h', header, 14, right)
        struct.pack_into('>h', header, 16, n)                    # n_coordinates

        # Coordinate arrays (relative to left/top)
        x_offsets = bytearray()
        y_offsets = bytearray()
        for i in range(n):
            x_offsets += struct.pack('>h', xs[i] - left)
            y_offsets += struct.pack('>h', ys[i] - top)

        return bytes(header) + bytes(x_offsets) + bytes(y_offsets)

    def export_as_tif(self):
        """Export current image + annotations as Fiji-compatible TIF."""
        if not self._current_file or self.current_result is None:
            if not self._current_file:
                self.statusBar().showMessage("没有打开的文件")
                return
            # If file loaded but not analyzed, just read data
            try:
                data, _, _ = read_image(self._current_file)
            except Exception as e:
                self.statusBar().showMessage(f"读取文件失败: {e}")
                return
        else:
            data = self.current_result.get("data")
            if data is None:
                try:
                    data, _, _ = read_image(self._current_file)
                except Exception as e:
                    self.statusBar().showMessage(f"读取文件失败: {e}")
                    return

        # Default save name
        base = os.path.splitext(os.path.basename(self._current_file))[0]
        default_name = base + "_annotated.tif"

        filepath, _ = QFileDialog.getSaveFileName(
            self, "导出 TIF 文件", default_name,
            "TIFF 文件 (*.tif *.tiff);;所有文件 (*)"
        )
        if not filepath:
            return

        try:
            _tifffile = __import__('importlib').import_module('tifffile')

            # Build ImageJ metadata
            ij_meta = {
                'ImageJ': '1.54p',
                'images': data.shape[0],
                'channels': data.shape[0],
                'mode': 'composite',
            }

            # Build ROI data from annotations
            points = self.viewer.get_annotations()
            roi_bytes = self._build_imagej_roi_bytes(points) if points else None

            # Write TIFF with ImageJ metadata
            _tifffile.imwrite(
                filepath,
                data,
                imagej=True,
                metadata=ij_meta,
            )

            # If we have ROI data, inject it into the TIFF
            if roi_bytes:
                self._inject_roi_into_tif(filepath, roi_bytes)

            n_ann = len(points) if points else 0
            self.statusBar().showMessage(
                f"已导出: {os.path.basename(filepath)}  ({data.shape[0]}通道, {n_ann}个标注)"
            )
        except Exception as e:
            self.statusBar().showMessage(f"导出失败: {e}")
            QMessageBox.critical(self, "导出错误", str(e))

    def _inject_roi_into_tif(self, filepath, roi_bytes):
        """Inject ImageJ ROI data into an existing TIFF file's metadata."""
        # ImageJ stores ROI in TIFF tag 50838 (IJMetadataByteCounts) and 50839 (IJMetadata)
        # The simplest approach: re-read and re-write with metadata
        try:
            _tifffile = __import__('importlib').import_module('tifffile')
            # Read back
            with _tifffile.TiffFile(filepath) as tif:
                data = tif.asarray()
                ij_meta = dict(tif.imagej_metadata) if tif.imagej_metadata else {}
            # Add ROI
            ij_meta['ROI'] = roi_bytes
            # Re-write
            _tifffile.imwrite(filepath, data, imagej=True, metadata=ij_meta)
        except Exception:
            pass  # ROI injection failed silently, image is still saved

    def _load_annotations_json(self, filepath):
        """Load annotations from .annotations.json file if it exists."""
        json_path = filepath + ".annotations.json"
        if not os.path.isfile(json_path):
            return []
        try:
            with open(json_path, "r", encoding="utf-8") as f:
                data = json.load(f)
            return [tuple(p) for p in data.get("points", [])]
        except Exception:
            return []

